

#Comparison Operators

# == equal
print(2 == 2)
print(1 == 0)

# != not equal
print (2 != 1)
print(2 != 2)

# greater than >
print(2 > 1)
print(2 > 4)

# less than <
print(2 < 4)
print(2 < 1)

#Greater than or equal to >=
print(2 >= 2)
print(2 >= 1)
print(3 >= 4)

# less than or equal to <=
print(2 <= 2)
print(2 <= 4)